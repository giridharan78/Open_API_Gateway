function RandomFname() {

          var chars = "abcdefghiklmnopqrstuvwxyzaabbccddeeffgghhiijjkkllmmnnooppqqrrssttuuvvwwxxyyzz";
          var string_length = 8;
          var RString = '';
          for (var i=0; i<string_length; i++) {

                   var rnum = Math.floor(Math.random() * chars.length);
                   RString += chars.substring(rnum,rnum+1);
          }
          return RString
		  }
